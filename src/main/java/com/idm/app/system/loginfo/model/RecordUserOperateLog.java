package com.idm.app.system.loginfo.model;

import java.io.Serializable;
import java.util.Date;

/**
 * @class 用户操作日志实体类
 * @author wengjiawie
 * @date 2019年2月25日
 */
public class RecordUserOperateLog implements Serializable{
	private static final long serialVersionUID = 1L;
	
	private String id;
	private String userId;
	private String operatTime;
	private String operatModule;
	private String operatType;
	private String operatData;
	private String createTime;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getUserId() {
		return userId;
	}
	public void setUserId(String userId) {
		this.userId = userId;
	}
	public String getOperatTime() {
		return operatTime;
	}
	public void setOperatTime(String operatTime) {
		this.operatTime = operatTime;
	}
	public String getOperatModule() {
		return operatModule;
	}
	public void setOperatModule(String operatModule) {
		this.operatModule = operatModule;
	}
	public String getOperatType() {
		return operatType;
	}
	public void setOperatType(String operatType) {
		this.operatType = operatType;
	}
	public String getOperatData() {
		return operatData;
	}
	public void setOperatData(String operatData) {
		this.operatData = operatData;
	}
	public String getCreateTime() {
		return createTime;
	}
	public void setCreateTime(String createTime) {
		this.createTime = createTime;
	}

}
