package com.idm.app.system.exceptioninfo.model;

import java.io.Serializable;

public class ErrBaseInfo  implements Serializable {

    private String errCode;
    private String reasonMsg;
    private String solutionMsg;
    private String errMsg;

    public String getErrCode() {
        return errCode;
    }

    public void setErrCode(String errCode) {
        this.errCode = errCode;
    }

    public String getReasonMsg() {
        return reasonMsg;
    }

    public void setReasonMsg(String reasonMsg) {
        this.reasonMsg = reasonMsg;
    }

    public String getSolutionMsg() {
        return solutionMsg;
    }

    public void setSolutionMsg(String solutionMsg) {
        this.solutionMsg = solutionMsg;
    }

    public String getErrMsg() {
        return errMsg;
    }

    public void setErrMsg(String errMsg) {
        this.errMsg = errMsg;
    }
}
