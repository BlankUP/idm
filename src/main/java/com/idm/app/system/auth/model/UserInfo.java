package com.idm.app.system.auth.model;

import java.io.Serializable;
import java.util.Date;

public class UserInfo implements Serializable{
	private static final long serialVersionUID = 1L;
	
	private String userId;
	private String userName;
	private String userPwd;
	private String PwdInitFlag;
	private String userStatus;
	private String loginToken;
	private Date tokenFailureTime;
	private String createTime;
	private String updateTime;
	private String createUser;
	private String updateUser;
	private Date pwdUpdateTime;
	private String depNo;
	
	private String depName;
	
	public String getUserId() {
		return userId;
	}
	public void setUserId(String userId) {
		this.userId = userId;
	}
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	public String getUserPwd() {
		return userPwd;
	}
	public void setUserPwd(String userPwd) {
		this.userPwd = userPwd;
	}
	public String getPwdInitFlag() {
		return PwdInitFlag;
	}
	public void setPwdInitFlag(String pwdInitFlag) {
		PwdInitFlag = pwdInitFlag;
	}
	public String getUserStatus() {
		return userStatus;
	}
	public void setUserStatus(String userStatus) {
		this.userStatus = userStatus;
	}
	public String getLoginToken() {
		return loginToken;
	}
	public void setLoginToken(String loginToken) {
		this.loginToken = loginToken;
	}
	public Date getTokenFailureTime() {
		return tokenFailureTime;
	}
	public void setTokenFailureTime(Date tokenFailureTime) {
		this.tokenFailureTime = tokenFailureTime;
	}
	public String getCreateTime() {
		return createTime;
	}
	public void setCreateTime(String createTime) {
		this.createTime = createTime;
	}
	public String getUpdateTime() {
		return updateTime;
	}
	public void setUpdateTime(String updateTime) {
		this.updateTime = updateTime;
	}
	public String getCreateUser() {
		return createUser;
	}
	public void setCreateUser(String createUser) {
		this.createUser = createUser;
	}
	public String getUpdateUser() {
		return updateUser;
	}
	public void setUpdateUser(String updateUser) {
		this.updateUser = updateUser;
	}
	public Date getPwdUpdateTime() {
		return pwdUpdateTime;
	}
	public void setPwdUpdateTime(Date pwdUpdateTime) {
		this.pwdUpdateTime = pwdUpdateTime;
	}
	public String getDepNo() {
		return depNo;
	}
	public void setDepNo(String depNo) {
		this.depNo = depNo;
	}
	public String getDepName() {
		return depName;
	}
	public void setDepName(String depName) {
		this.depName = depName;
	}
	@Override
	public String toString() {
		return "UserInfo [userId=" + userId + ", userName=" + userName + ", userPwd=" + userPwd + ", PwdInitFlag="
				+ PwdInitFlag + ", userStatus=" + userStatus + ", loginToken=" + loginToken + ", tokenFailureTime="
				+ tokenFailureTime + ", createTime=" + createTime + ", updateTime=" + updateTime + ", createUser="
				+ createUser + ", updateUser=" + updateUser + ", pwdUpdateTime=" + pwdUpdateTime + ", depNo=" + depNo
				+ ", depName=" + depName + "]";
	}
	

}
