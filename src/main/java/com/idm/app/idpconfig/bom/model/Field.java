package com.idm.app.idpconfig.bom.model;

import java.io.Serializable;


public class Field implements Serializable {
	 	/**
		 * 
		 */
	private static final long serialVersionUID = 1L;
		private String fieldId ;
		private String interfaceCode;
		private String  fieldName;
		private String fieldType ;
		private String isChildNode ;
		private String fatherNode ;
		private String fieldText ;
		private String createTime;
		private String status;//有效无效状态
		
		public String getFieldId() {
			return fieldId;
		}
		public void setFieldId(String fieldId) {
			this.fieldId = fieldId;
		}
		public String getInterfaceCode() {
			return interfaceCode;
		}
		public void setInterfaceCode(String interfaceCode) {
			this.interfaceCode = interfaceCode;
		}
		public String getFieldName() {
			return fieldName;
		}
		public void setFieldName(String fieldName) {
			this.fieldName = fieldName;
		}
		public String getFieldType() {
			return fieldType;
		}
		public void setFieldType(String fieldType) {
			this.fieldType = fieldType;
		}
		public String getIsChildNode() {
			return isChildNode;
		}
		public void setIsChildNode(String isChildNode) {
			this.isChildNode = isChildNode;
		}
		public String getFatherNode() {
			return fatherNode;
		}
		public void setFatherNode(String fatherNode) {
			this.fatherNode = fatherNode;
		}
		public String getFieldText() {
			return fieldText;
		}
		public void setFieldText(String fieldText) {
			this.fieldText = fieldText;
		}
		public String getCreateTime() {
			return createTime;
		}
		public void setCreateTime(String createTime) {
			this.createTime = createTime;
		}
		public String getStatus() {
			return status;
		}
		public void setStatus(String status) {
			this.status = status;
		}
		

}
