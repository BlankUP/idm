package com.idm.app.idpconfig.proModelParaConfig.model;

import java.io.Serializable;

/**
* <p>Description:参数字段实体类 </p>  
* <p>Company: </p>  
* @author caiwenjian  
* @date 2019年2月25日  
* @version 1.0
 */
public class ParaBaseInfo  implements Serializable {
	private static final long serialVersionUID = -5488180638027458188L;
	private String fieldid;	             //参数ID
    private String parameterfields;	 //参数字段英文
    private String parameternamech;	 //参数字段中文名
    private String parametertype;	 //参数类型
    private String createuser;       //创建人
    private String updateuser;	     //修改人
    private String createtime;	     //创建日期
    private String updatetime;	     //修改日期
    private String status;	         //状态0:有效，1:无效
    private int page;				 //第几页
	private int size;				 //每页条数
	private int rnm;				 
    public String getFieldid() {
		return fieldid;
	}
	public void setFieldid(String fieldid) {
		this.fieldid = fieldid;
	}
	public String getParameterfields() {
		return parameterfields;
	}
	public void setParameterfields(String parameterfields) {
		this.parameterfields = parameterfields;
	}
	public String getParameternamech() {
		return parameternamech;
	}
	public void setParameternamech(String parameternamech) {
		this.parameternamech = parameternamech;
	}
	public String getParametertype() {
		return parametertype;
	}
	public void setParametertype(String parametertype) {
		this.parametertype = parametertype;
	}
	public String getCreateuser() {
		return createuser;
	}
	public void setCreateuser(String createuser) {
		this.createuser = createuser;
	}
	public String getUpdateuser() {
		return updateuser;
	}
	public void setUpdateuser(String updateuser) {
		this.updateuser = updateuser;
	}
	public String getCreatetime() {
		return createtime;
	}
	public void setCreatetime(String createtime) {
		this.createtime = createtime;
	}
	public String getUpdatetime() {
		return updatetime;
	}
	public void setUpdatetime(String updatetime) {
		this.updatetime = updatetime;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	
	@Override
	public String toString() {
		return "ParaBaseInfo [fieldid=" + fieldid + ", parameterfields="
				+ parameterfields + ", parameternamech=" + parameternamech
				+ ", parametertype=" + parametertype + ", createuser="
				+ createuser + ", updateuser=" + updateuser + ", createtime="
				+ createtime + ", updatetime=" + updatetime + ", status="
				+ status + "]";
	}
	
	public int getPage() {
		return page;
	}
	
	public void setPage(int page) {
		this.page = page;
	}
	
	public int getSize() {
		return size;
	}
	
	public void setSize(int size) {
		this.size = size;
	}

	
	public int getRnm() {
		return rnm;
	}
	
	public void setRnm(int rnm) {
		this.rnm = rnm;
	}
	
}
