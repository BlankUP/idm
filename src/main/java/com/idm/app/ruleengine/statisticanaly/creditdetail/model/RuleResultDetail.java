package com.idm.app.ruleengine.statisticanaly.creditdetail.model;

import java.io.Serializable;

public class RuleResultDetail implements Serializable {

    private String paperNo;

    private String ruleCode;

    private String ruleName;

    private String fieldValue;

    private String ruleResult;

    private String ruleReason;

    private String queryDate;

    public String getPaperNo() {
        return paperNo;
    }

    public void setPaperNo(String paperNo) {
        this.paperNo = paperNo;
    }

    public String getRuleCode() {
        return ruleCode;
    }

    public void setRuleCode(String ruleCode) {
        this.ruleCode = ruleCode;
    }

    public String getRuleName() {
        return ruleName;
    }

    public void setRuleName(String ruleName) {
        this.ruleName = ruleName;
    }

    public String getRuleResult() {
        return ruleResult;
    }

    public void setRuleResult(String ruleResult) {
        this.ruleResult = ruleResult;
    }

    public String getRuleReason() {
        return ruleReason;
    }

    public void setRuleReason(String ruleReason) {
        this.ruleReason = ruleReason;
    }

    public String getQueryDate() {
        return queryDate;
    }

    public void setQueryDate(String queryDate) {
        this.queryDate = queryDate;
    }

    public String getFieldValue() {
        return fieldValue;
    }

    public void setFieldValue(String fieldValue) {
        this.fieldValue = fieldValue;
    }
}
